#include "./libft.h"

int			*create_mas(int len)
{
	int	*mas;
	int n;

	n = 0;
	mas = (int *)malloc(sizeof(int) * len);
	while (n < len)
	{
		mas[n] = 0;
		n++;
	}
	return (mas);
}
