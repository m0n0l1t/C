#include "./libft.h"

void		ft_alpha_up(char **str)
{
	int		n;

	n = 0;
	while ((*str)[n])
	{
		if (ft_str_is_alpha((*str)[n]))
			(*str)[n] = ft_abs((*str)[n]) - 32;
		n++;
	}
}
